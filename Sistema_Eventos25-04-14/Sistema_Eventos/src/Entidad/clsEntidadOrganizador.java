public class clsEntidadOrganizador {
    
    private String id_org;
    private String nomb_org;
    private String apel_org;
    private String dni_org;
    private String cel_org;

    public String getId_org() {
        return id_org;
    }

    public void setId_org(String id_org) {
        this.id_org = id_org;
    }

    public String getNomb_org() {
        return nomb_org;
    }

    public void setNomb_org(String nomb_org) {
        this.nomb_org = nomb_org;
    }

    public String getApel_org() {
        return apel_org;
    }

    public void setApel_org(String apel_org) {
        this.apel_org = apel_org;
    }

    public String getDni_org() {
        return dni_org;
    }

    public void setDni_org(String dni_org) {
        this.dni_org = dni_org;
    }

    public String getCel_org() {
        return cel_org;
    }

    public void setCel_org(String cel_org) {
        this.cel_org = cel_org;
    }
    
    
    
}
