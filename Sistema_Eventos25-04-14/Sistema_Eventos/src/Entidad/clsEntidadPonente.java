public class clsEntidadPonente {
    
     private String id_pon;
     private String nomb_pon;
     private String ape_pon;
     private String nacionalidad_pon;
     private String especialidad_pon;
     private String foto_pon;

    public String getId_pon() {
        return id_pon;
    }

    public void setId_pon(String id_pon) {
        this.id_pon = id_pon;
    }

    public String getNomb_pon() {
        return nomb_pon;
    }

    public void setNomb_pon(String nomb_pon) {
        this.nomb_pon = nomb_pon;
    }

    public String getApe_pon() {
        return ape_pon;
    }

    public void setApe_pon(String ape_pon) {
        this.ape_pon = ape_pon;
    }

    public String getNacionalidad_pon() {
        return nacionalidad_pon;
    }

    public void setNacionalidad_pon(String nacionalidad_pon) {
        this.nacionalidad_pon = nacionalidad_pon;
    }

    public String getEspecialidad_pon() {
        return especialidad_pon;
    }

    public void setEspecialidad_pon(String especialidad_pon) {
        this.especialidad_pon = especialidad_pon;
    }

    public String getFoto_pon() {
        return foto_pon;
    }

    public void setFoto_pon(String foto_pon) {
        this.foto_pon = foto_pon;
    }
    
     
}
